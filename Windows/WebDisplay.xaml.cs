using System;
using System.Windows;


// ---------------------------------------------------------------------------------------------------------------------
namespace TrashWizard.Windows
{
  // ---------------------------------------------------------------------------------------------------------------------
  // ---------------------------------------------------------------------------------------------------------------------
  // ---------------------------------------------------------------------------------------------------------------------
  public partial class WebDisplay : Window
  {
    // ---------------------------------------------------------------------------------------------------------------------
    public WebDisplay(Window toParent, string tcURL, int tnHeight, int tnWidth)
    {
      this.InitializeComponent();

      this.Owner = toParent;
      this.Icon = toParent.Icon;

      this.Height = tnHeight;
      this.Width = tnWidth;

      this.foWebView.Navigate(new Uri(tcURL));
    }

    // ---------------------------------------------------------------------------------------------------------------------
    private void btnOk_Click(object toSender, RoutedEventArgs teRoutedEventArgs)
    {
      this.DialogResult = true;
    }

    // ---------------------------------------------------------------------------------------------------------------------
  }

  // ---------------------------------------------------------------------------------------------------------------------
  // ---------------------------------------------------------------------------------------------------------------------
  // ---------------------------------------------------------------------------------------------------------------------
}
